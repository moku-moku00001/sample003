public class Question05_02 {

	public static void main(String[] args) {
		int[][] num = {{ 10, 20, 30 }, { 40, 50, 60 }}; 
		 
		 System.out.println("１段目の１つ目の値は" + num[0][0]); 
		 System.out.println("１段目の２つ目の値は" + num[0][1]); 
		 System.out.println("１段目の３つ目の値は" + num[0][2]);
		 System.out.println("２段目の１つ目の値は" + num[1][0]);
		 System.out.println("２段目の２つ目の値は" + num[1][1]);
		 System.out.println("２段目の３つ目の値は" + num[1][2]);
		 
		 System.out.println("処理を終了します"); 

	}

}
